"""
¿Cómo actualizar los datos?
- Un diccionario se puede actualizar de tres formas: añadiendo un nuevo par (clave-valor), modificando el valor para una clave o eliminando un par. El siguiente código muestra cómo añadir un nuevo par y modificar uno existente:

"""
#EJEMPLO
d = {'nombre':'Zara','edad':7,'clase':'First'}
d['edad']=8; #actualizando una clave que ya existe
d['colegio'] = "DPS colegio" #añadir un nuevo par

print("d['edad']: ", d['edad'])
print("d['colegio']: ", d['colegio'])