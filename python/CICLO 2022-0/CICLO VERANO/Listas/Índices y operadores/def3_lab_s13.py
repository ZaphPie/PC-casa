#Agregar elementos:append e insert.
#append: agregará el elemento por el final de la lista.
lista_letras = ['a','b','c','d','e','f','g','h','i','j','k']
print(lista_letras)
lista_letras.append('n')
lista_letras.append('o')
lista_letras.append('p')
print(lista_letras)
print("-----------------------------------")
#insert: agregará el elemento de acuerdo al parámetro especificado.
lista_letras=['a','b','c','d','e','f','g','h','i','j','k']
print(lista_letras)
lista_letras.insert(1,'A')
lista_letras.insert(3,'B')
lista_letras.insert(5,'C')
print(lista_letras)